"""
石头剪刀布 —— Python 入门练手项目

游戏规则：
  石头 赢 剪刀
  剪刀 赢 布
  布 赢 石头

玩家输入选择，电脑随机出拳，统计胜负记录。
"""

import random


# 出拳选项
拳法 = ["石头", "剪刀", "布"]


def 显示规则():
    """打印游戏规则"""
    print("=" * 40)
    print("✊✌️✋ 石头剪刀布")
    print("  石头 赢 剪刀  |  剪刀 赢 布  |  布 赢 石头")
    print("  输入 q 可随时退出")
    print("=" * 40)


def 判断胜负(玩家, 电脑):
    """返回胜负结果：'胜'、'负'、'平'"""
    if 玩家 == 电脑:
        return "平"
    if (玩家 == "石头" and 电脑 == "剪刀") or \
       (玩家 == "剪刀" and 电脑 == "布") or \
       (玩家 == "布" and 电脑 == "石头"):
        return "胜"
    return "负"


def 石头剪刀布游戏():
    """主游戏逻辑"""
    显示规则()

    胜 = 0
    负 = 0
    平 = 0
    总场次 = 0

    while True:
        print(f"\n📊 战绩：{胜}胜 {负}负 {平}平（共{总场次}局）")

        玩家输入 = input("请出拳（1=石头 2=剪刀 3=布）：").strip()

        if 玩家输入.lower() == "q":
            break

        # 输入映射
        if 玩家输入 == "1":
            玩家出拳 = "石头"
        elif 玩家输入 == "2":
            玩家出拳 = "剪刀"
        elif 玩家输入 == "3":
            玩家出拳 = "布"
        else:
            print("⚠️  请输入 1/2/3，或输入 q 退出。")
            continue

        电脑出拳 = random.choice(拳法)
        结果 = 判断胜负(玩家出拳, 电脑出拳)
        总场次 += 1

        # 显示本局结果
        符号 = {"石头": "✊", "剪刀": "✌️", "布": "✋"}
        if 结果 == "胜":
            print(f"  你{符号[玩家出拳]} vs 电脑{符号[电脑出拳]} → 🎉 你赢了！")
            胜 += 1
        elif 结果 == "负":
            print(f"  你{符号[玩家出拳]} vs 电脑{符号[电脑出拳]} → 😞 你输了！")
            负 += 1
        else:
            print(f"  你{符号[玩家出拳]} vs 电脑{符号[电脑出拳]} → 🤝 平局！")
            平 += 1

    # 结算
    print("\n" + "=" * 40)
    print(f"🏁 最终战绩：{胜}胜 {负}负 {平}平（共{总场次}局）")
    if 总场次 > 0:
        胜率 = 胜 / 总场次 * 100
        print(f"📈 胜率：{胜率:.1f}%")
        if 胜率 >= 60:
            print("🌟 你是剪刀石头布高手！")
        elif 胜率 >= 40:
            print("👍 水平还行，继续加油！")
        else:
            print("💪 多练练运气会变好！")
    print("👋 下次再来！")


if __name__ == "__main__":
    石头剪刀布游戏()
